# ライブラリのインポート
import os, json
from pathlib import Path
from activities.s04_image_picker.obtain_picture import ImagePicker
from utils.azure import embedding_client, content_safety_client


# ----------------------------------
# 開発用メイン処理
# ----------------------------------
if __name__ == "__main__":
    keyword = "ペットSNS"
    max_images = 4
    print(f"Picking images for keyword: {keyword}")
    picker = ImagePicker(embedding_client, content_safety_client, similarity_threshold=0.10)
    images = picker.pick_images(keyword, max_images)
    print(f"Images: {images}")
    
    image_path = Path("data/images")
    os.makedirs(image_path, exist_ok=True)

    with open(image_path / f"{keyword}.json", "w", encoding="utf-8") as f:
        json.dump(images, f, ensure_ascii=False, indent=2)

    print(f"Images saved to {image_path / f"{keyword}.json"}")