# ライブラリのインポート
import os, json
from pathlib import Path
from activities.s01_web_crawler.scraper import ArticleScraper


# ----------------------------------
# 開発用メイン処理
# ----------------------------------
if __name__ == "__main__":
    keyword = "ペットSNS"
    scraper = ArticleScraper()
    print(f"Scraping keyword: {keyword}")
    titles, docs = scraper.scrape(keyword)

    print(f"Saving titles and docs to {keyword}")
    title_path = Path("data/titles")
    title_path.mkdir(parents=True, exist_ok=True)
    with open(title_path / f"{keyword}.json", "w", encoding="utf-8") as f:
        json.dump(titles, f, ensure_ascii=False, indent=2)

    doc_path = Path("data/docs")
    doc_path.mkdir(parents=True, exist_ok=True)
    with open(doc_path / f"{keyword}.json", "w", encoding="utf-8") as f:
        json.dump(docs, f, ensure_ascii=False, indent=2)
    print(f"Saved titles and docs to {keyword}")
