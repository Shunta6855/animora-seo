# ライブラリのインポート
import os, json
from pathlib import Path
from activities.s03_draft_generator.generator.generate_outline import validate_outline
from activities.s03_draft_generator.generator.generate_draft import generate_draft


# ----------------------------------
# 開発用メイン処理
# ----------------------------------
if __name__ == "__main__":
    keyword = "ペットSNS"
    outline_path = Path("data/outlines")
    draft_path = Path("data/drafts")
    
    print(f"Generating draft for keyword: {keyword}")
    outline = validate_outline(keyword)

    os.makedirs(outline_path, exist_ok=True)
    with open(outline_path / f"{keyword}.json", "w", encoding="utf-8") as f:
        json.dump(outline, f, ensure_ascii=False, indent=2)

    print(f"Generating draft for keyword: {keyword}")
    draft = generate_draft(keyword, outline["title"], outline["h2_list"])

    os.makedirs(draft_path, exist_ok=True)
    with open(draft_path / f"{keyword}.md", "w", encoding="utf-8") as f:
        f.write(draft)
    
    print(f"Draft saved to {draft_path / f"{keyword}.md"}")
